export type TranslationKey = keyof typeof translations.uz;

export function getTranslation(lang: Language, section: TranslationKey, key: string): string {
  const sectionObj = translations[lang]?.[section] || translations.en[section];
  const value = (sectionObj as any)?.[key];
  if (value) return value;
  const enValue = (translations.en[section] as any)?.[key];
  if (enValue) return enValue;
  return key;
}

